package com.scaler.dsa.timeComplexity;

public class Timecomplexity2 {

	public static void main(String[] args) {
		
		int n=1;
		for(int i = 0; i < n; i++){
			System.out.println("i="+i);
	        for(int j=0; j < i / 2; j++){
	            // O(1) operation
	        	System.out.println("j="+j);
	        }
	        System.out.println();
		}
	        
		}
	}
